a = 10
b = 3

# Aritmeticos

suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b
division_entera = a // b
modulo = a % b
exponenciacion = a ** b

print(suma)
print(resta)
print(multiplicacion)
print(division)
print(division_entera)
print(modulo)
print(exponenciacion)

# Comparación

igual = a == b
diferente = a != b
mayor = a > b
menor = a < b
mayor_igual = a >= b
menor_igual = a <= b

print(igual)
print(diferente)
print(mayor)
print(menor)
print(mayor_igual)
print(menor_igual)

# Lógicos

resultado_and = (a > 5) and (b < 5)
resultado_or = (a > 15) or (b < 5)
resultado_not = not (a > 5)

print(resultado_and)
print(resultado_or)
